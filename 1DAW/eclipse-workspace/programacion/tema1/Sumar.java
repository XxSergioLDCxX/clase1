package tema1;

public class Sumar {

	public static void main(String[] args) {

		double n1 = 3;
		double n2 = 7;
		double suma = n1 + n2;
		double resta = n1 - n2;
		double multi = n1 * n2;
		double divid = n1 / n2;

		System.out.println(suma);
		System.out.println(n1 + n2);
		System.out.println(resta);
		System.out.println(multi);
		System.out.println(divid);
	}

}
